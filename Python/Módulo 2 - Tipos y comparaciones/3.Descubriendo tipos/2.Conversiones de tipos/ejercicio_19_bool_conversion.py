# Ejercicio 19 - Funcion bool()
# bool() convierte una variable en booleano.
# Si la variable tiene contenido, el resultado es True.
# Si esta vacia o es 0, el resultado es False.

member = "Sam"
middle_name = ""
foot_size = 8.5
siblings = 0

boolean_member = bool(member)
boolean_middle_name = bool(middle_name)
boolean_foot_size = bool(foot_size)
boolean_siblings = bool(siblings)

print(boolean_member)
print(boolean_middle_name)
print(boolean_foot_size)
print(boolean_siblings)
