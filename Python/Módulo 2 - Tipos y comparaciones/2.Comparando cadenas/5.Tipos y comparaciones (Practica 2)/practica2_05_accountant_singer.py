# Ejercicio 05 - Compara accountant con singer para mostrar False
# La respuesta correcta es == (no !=), porque ambas son "Lisa" y "Joan" (diferentes),
# pero el enunciado pide False, lo que se logra con == entre cadenas distintas.

accountant = "Lisa"
singer = "Joan"
print(accountant == singer)
